func wireDotGo(proj *models.Project) *jen.File {
	code := jen.NewFile(packageName)

	utils.AddImports(proj, code, false)

	code.Add(
		jen.Var().ID("Providers").Op("=").ID("wire").Dot("NewSet").Call(jen.ID("ProvideService")),
		jen.Line(),
	)

	return code
}
